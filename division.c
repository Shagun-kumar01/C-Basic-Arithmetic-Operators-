// Program to demonstrate Division Operator 
#include <stdio.h>

int main () {
  // Declared total pieces of cake
  int Total_cake_pieces = 20;

  // total friends
  int No_of_friends = 10;
  
  // Distribute_cake_between_friends
  int Per_person_pieces_of_cake = Total_cake_pieces / No_of_friends;

printf("Every Friend received pieces of cake: %d\n", Per_person_pieces_of_cake);

return 0;

}
